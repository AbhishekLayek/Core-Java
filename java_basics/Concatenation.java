package java_basics;

public class Concatenation {
	public static void main(String[] args) {
		// In Java Combination of Integer and String, or Decimal and String or String and String is called concatenation
		
		System.out.println("The Value Is " + 10);
		System.out.println("The Value Of PI Is " + 3.14);
		System.out.println("Hello Everyone. " + "Welcome To Java");
	}
}

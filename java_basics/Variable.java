/* Variable: Variable is a named memory location, which is used to store some data or value and it can be change
 * multiple times during execution */

package java_basics;

public class Variable {
	public static void main(String[] args) {
		
		int var; // Variable Declaration
		
		var = 10; // Variable Initialization
		
		System.out.println(var); // Variable Utilization
		
		int val = 20; // Variable Declaration and Initialization in same line
		
		System.out.println(val);
	}
}

package operators;

public class ShorthandOperator {
	public static void main(String[] args) {
		int a = 10;
		
		a += 2; // Addition Assignment(a = a + 2)
		a -= 2; // Subtraction Assignment(a = a - 2)
		a *= 2; // Multiplication Assignment(a = a * 2)
		a /= 2; // Division Assignment(a = a / 2)
		a %= 2; // Remainder Assignment(a = a % 2)
		
		System.out.println(a);
		
	}
}
